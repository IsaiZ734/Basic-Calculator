class Calculator:
    def __init__(self):
        pass

    def add(self, num1, num2):
        return num1 + num2
    
    def substraction(self, num1, num2):
        return num1 - num2
    
    def multiplication(self, num1, num2):
        return num1 * num2
    
    def division(self, num1, num2):
        return num1 / num2